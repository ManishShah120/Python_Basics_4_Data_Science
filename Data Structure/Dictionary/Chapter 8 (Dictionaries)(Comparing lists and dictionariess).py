'''
Dictionaries are like lists except that they use KEYS
instead of numbers to look up values.
'''
#######LIST#########
print('LIST')
lst = list()
lst.append(12)
lst.append(183)
print(lst)
lst[0] = 23
print(lst)
'''
####LIST=lst####
Key       Value
[0]         21
[1]         183
'''





######DICTIONARIES############
print('Dictionaries')
ddd = dict()
ddd['age'] = 21
ddd['course'] = 182
print(ddd)
ddd['age'] = 23
print(ddd)
####Dictionary = ddd#####
'''
Key                Value
['course']          182
['age']             21
'''